
public class PriorityQueueException extends RuntimeException {

	public PriorityQueueException() {
		super();
	}

	public PriorityQueueException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PriorityQueueException(String message, Throwable cause) {
		super(message, cause);
	}

	public PriorityQueueException(String message) {
		super(message);
	}

	public PriorityQueueException(Throwable cause) {
		super(cause);
	}
	
}
